def bflim2gtx(name):
    fp = open(name , "rb")
    fp.seek(-0x28,2)
    sig = fp.read(4)
    endian  = fp.read(2)
    null = fp.read(2)
    allSize = struct.unpack(">I" ,fp.read(4))[0]
    fp.seek(-0xc,2)
    WIDTH = struct.unpack(">H" ,fp.read(2))[0]
    HEIGHT = struct.unpack(">H" ,fp.read(2))[0]
    unk = struct.unpack(">H" ,fp.read(2))[0]
    FORMAT = ord(fp.read(1))
    SWIZZLE = ord(fp.read(1))
    FLIMSIZE = struct.unpack(">I" ,fp.read(4))[0]
    fp.seek(0,0)
    DATA = fp.read(FLIMSIZE)
    print(hex(len(DATA)))
    SWIZZLE = (SWIZZLE - 4 )/ 0x20
    TMP = {0x5:0x8, #RGB565
           0x9:0x1a, #RGBA8888
           0xc:0x31,#DXT1
           0xd:0x32,#DXT3
           0xe:0x33,#DXT5
           0xf:0x34,#BC4
           0x10:0x34,#BC4 HIGH
           0x11:0x35,#BC5
           0x14:0x1a,#RGBA32
           0x15:0x31,#DXT1 32BIT
           0x16:0x32,#DXT3 32BIT
           0x17:0x33}#DXT5 32BIT

    dest = open("gtx\\%s.gtx"%(name), "wb")
    dest.write(GTX_header)
    dest.write(DATA)
    dest.write(GTX_end)

    if FORMAT in TMP:
        FORMAT = TMP[FORMAT]
    else:
        print("Error FORMAT:%X"%FORMAT)
        return
    dest.seek(0x46)
    dest.write(struct.pack(">H" , WIDTH))
    dest.seek(0x7E)
    dest.write(struct.pack(">H" , WIDTH))
    dest.seek(0x4A)
    dest.write(struct.pack(">H" , HEIGHT))
    dest.seek(0x60)
    dest.write(struct.pack(">I" , FLIMSIZE))
    dest.seek(0xF0)
    dest.write(struct.pack(">I" , FLIMSIZE))
    dest.seek(0x57)
    dest.write(chr(FORMAT))
    dest.seek(0x76)
    dest.write(chr(SWIZZLE))
    dest.close()
    fp.close()


